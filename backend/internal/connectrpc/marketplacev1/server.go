// ── Search ─────────────────────────────────────────────────────────────────────

func (s *Server) Search(ctx context.Context, req *connect.Request[SearchRequest]) (*connect.Response[SearchResponse], error) {
	query := strings.TrimSpace(req.Msg.GetQuery())
	if len(query) < 2 {
		return nil, connect.NewError(connect.CodeInvalidArgument, errString("query must be at least 2 characters"))
	}
	limit := int(req.Msg.GetLimit())
	if limit <= 0 || limit > 50 {
		limit = 20
	}

	rows, err := s.q.Search(ctx, query, limit)
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, err)
	}

	results := make([]*SearchResult, 0, len(rows))
	for i := range rows {
		results = append(results, &SearchResult{
			Kind:       rows[i].Kind,
			Collection: rows[i].Collection,
			TokenId:    rows[i].TokenID,
			Name:       rows[i].Name,
			ImageUri:   rows[i].ImageURI,
		})
	}
	if results == nil {
		results = []*SearchResult{}
	}

	return connect.NewResponse(&SearchResponse{Results: results}), nil
}

