// ── Helpers ─────────────────────────────────────────────────────────────────

func isNotFound(err error) bool {
	if err == nil {
		return false
	}
	s := err.Error()
	return strings.Contains(s, "not found") || strings.Contains(s, "no rows")
}

func errString(msg string) error {
	return fmt.Errorf("%s", msg)
}

func tokenActivityToActivityRow(rows []db.TokenActivityRow, coll, tokID string) []db.ActivityRow {
	out := make([]db.ActivityRow, len(rows))
	for i, tr := range rows {
		out[i] = db.ActivityRow{
			Type: tr.Type, Collection: coll, TokenID: tokID,
			AmountWei: tr.AmountWei, Timestamp: tr.Timestamp, TxHash: tr.TxHash,
		}
	}
	return out
}

func offerRowToProto(row *db.OfferRow) *Offer {
	o := &Offer{
		OfferId:    row.OfferID,
		Bidder:     row.Bidder,
		Collection: row.Collection,
		TokenId:    row.TokenID,
		AmountWei:  row.AmountWei,
		FeeWei:     row.FeeWei,
		Units:      row.Units,
		Standard:   row.Standard,
		Status:     row.Status,
		MakeTx:     row.MakeTx,
	}
	if !row.ExpiresAt.IsZero() {
		o.ExpiresAtMs = row.ExpiresAt.UnixMilli()
	}
	if !row.CreatedAt.IsZero() {
		o.CreatedAtMs = row.CreatedAt.UnixMilli()
	}
	return o
}
