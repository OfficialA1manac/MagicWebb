// ── ListCollections (server-streaming) ───────────────────────────────────────

func (s *Server) ListCollections(ctx context.Context, req *connect.Request[ListCollectionsRequest], stream *connect.ServerStream[Collection]) error {
	limit := int(req.Msg.GetLimit())
	if limit <= 0 || limit > 200 {
		limit = 50
	}

	rows, err := s.q.ListCollections(ctx, limit)
	if err != nil {
		return connect.NewError(connect.CodeInternal, err)
	}

	// Collect addresses for batch stats (single round-trip after streaming basics).
	cols := make([]*Collection, 0, len(rows))
	addrs := make([]string, 0, len(rows))
	for i := range rows {
		cols = append(cols, &Collection{
			Address:     rows[i].Address,
			Name:        rows[i].Name,
			Symbol:      rows[i].Symbol,
			Standard:    rows[i].Standard,
			DeployBlock: int64(rows[i].DeployBlock),
			Verified:    rows[i].Verified,
		})
		addrs = append(addrs, rows[i].Address)
	}

	// Batch-fill stats: one round-trip for floor + listed count + volume.
	if stats, err := s.q.GetCollectionStatsBatch(ctx, addrs); err == nil {
		for i, c := range cols {
			if s, ok := stats[c.Address]; ok {
				cols[i].FloorPriceWei = s.FloorPriceWei
				cols[i].Volume_24HWei = s.Volume24hWei
				cols[i].ListedCount = int32(s.ListedCount)
			}
		}
	}

	// Stream each collection to the client as it's ready.
	for _, c := range cols {
		if err := stream.Send(c); err != nil {
			return err
		}
	}
	return nil
}

