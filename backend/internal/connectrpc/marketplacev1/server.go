// Package marketplacev1 provides a Connect-RPC server that implements the
// MarketplaceService defined in marketplace.proto. Each handler performs a
// direct DB query and returns the result as a typed Protobuf message —
// no JSON marshal/unmarshal, no manual HTTP status code handling.
//
// The handler is mounted via an adapter in the api package so it works with
// Fiber's fasthttp underpinnings. Clients can call it using Connect-RPC
// (JSON or Protobuf, over HTTP/1.1 or HTTP/2), gRPC, or gRPC-Web.
package marketplacev1

import (
	"context"
	"fmt"
	"strings"

	"connectrpc.com/connect"

	"github.com/OfficialA1manac/MagicWebb/backend/internal/db"
	"github.com/OfficialA1manac/MagicWebb/backend/internal/sse"
)

// Server implements the MarketplaceServiceHandler interface generated by
// protoc-gen-connect-go. It wraps a *db.Q for read queries and an optional
// SSE Broadcaster for streaming subscription handlers.
type Server struct {
	q     *db.Q
	bcast *sse.Broadcaster
}

// NewServer creates a MarketplaceService handler backed by the given DB.
// bcast is optional — when nil, streaming subscription RPCs return
// Unavailable errors to clients.
func NewServer(q *db.Q, bcast *sse.Broadcaster) *Server {
	return &Server{q: q, bcast: bcast}
}

// ── GetListing ──────────────────────────────────────────────────────────────

func (s *Server) GetListing(ctx context.Context, req *connect.Request[GetListingRequest]) (*connect.Response[GetListingResponse], error) {
	collection := strings.ToLower(req.Msg.GetCollection())
	tokenID := req.Msg.GetTokenId()

	row, err := s.q.GetListing(ctx, collection, tokenID)
	if err != nil {
		if isNotFound(err) {
			return nil, connect.NewError(connect.CodeNotFound, err)
		}
		return nil, connect.NewError(connect.CodeInternal, err)
	}

	res := &GetListingResponse{
		Collection: row.Collection,
		TokenId:    row.TokenID,
		Seller:     row.Seller,
		PriceWei:   row.PriceWei,
		Amount:     row.Amount,
		Standard:   row.Standard,
		TxHash:     row.TxHash,
		Name:       row.Name,
		ImageUri:   row.ImageURI,
	}
	if !row.ExpiresAt.IsZero() {
		res.ExpiresAtMs = row.ExpiresAt.UnixMilli()
	}
	if !row.ListedAt.IsZero() {
		res.ListedAtMs = row.ListedAt.UnixMilli()
	}

	return connect.NewResponse(res), nil
}

