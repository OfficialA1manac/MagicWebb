// ── GetActivity ──────────────────────────────────────────────────────────────

func (s *Server) GetActivity(ctx context.Context, req *connect.Request[GetActivityRequest]) (*connect.Response[GetActivityResponse], error) {
	limit := int(req.Msg.GetLimit())
	if limit <= 0 || limit > 200 {
		limit = 50
	}
	address := strings.ToLower(req.Msg.GetAddress())
	collection := strings.ToLower(req.Msg.GetCollection())
	tokenID := req.Msg.GetTokenId()

	var rows []db.ActivityRow
	var err error

	// Route to correct query based on filter params.
	switch {
	case collection != "" && tokenID != "" && address != "":
		tokenRows, terr := s.q.GetTokenActivityByAddress(ctx, collection, tokenID, address, limit)
		if terr != nil {
			return nil, connect.NewError(connect.CodeInternal, terr)
		}
		rows = tokenActivityToActivityRow(tokenRows, collection, tokenID)
	case collection != "" && tokenID != "":
		tokenRows, terr := s.q.GetTokenActivity(ctx, collection, tokenID, limit)
		if terr != nil {
			return nil, connect.NewError(connect.CodeInternal, terr)
		}
		rows = tokenActivityToActivityRow(tokenRows, collection, tokenID)
	case address != "":
		rows, err = s.q.GetRecentTransactionsByAddress(ctx, address, limit)
	default:
		rows, err = s.q.GetRecentTransactions(ctx, limit)
	}
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, err)
	}

	events := make([]*ActivityEvent, 0, len(rows))
	for i := range rows {
		events = append(events, &ActivityEvent{
			Type:        rows[i].Type,
			Collection:  rows[i].Collection,
			TokenId:     rows[i].TokenID,
			AmountWei:   rows[i].AmountWei,
			TimestampMs: rows[i].Timestamp.UnixMilli(),
			TxHash:      rows[i].TxHash,
		})
	}
	if events == nil {
		events = []*ActivityEvent{}
	}

	return connect.NewResponse(&GetActivityResponse{Events: events}), nil
}

