// ── ListListings (server-streaming) ──────────────────────────────────────────

func (s *Server) ListListings(ctx context.Context, req *connect.Request[ListListingsRequest], stream *connect.ServerStream[Listing]) error {
	f := db.ListingsFilter{
		Collection:  strings.ToLower(req.Msg.GetCollection()),
		Seller:      strings.ToLower(req.Msg.GetSeller()),
		Sort:        req.Msg.GetSort(),
		MinPriceWei: req.Msg.GetMinPriceWei(),
		MaxPriceWei: req.Msg.GetMaxPriceWei(),
	}
	if f.Sort == "" {
		f.Sort = "recent"
	}
	if limit := int(req.Msg.GetLimit()); limit > 0 && limit <= 100 {
		f.Limit = limit
	} else {
		f.Limit = 50
	}
	if traits := req.Msg.GetTraits(); traits != "" {
		f.Traits = make(map[string]string)
		for _, pair := range strings.Split(traits, ",") {
			parts := strings.SplitN(pair, ":", 2)
			if len(parts) == 2 && parts[0] != "" && parts[1] != "" {
				f.Traits[parts[0]] = parts[1]
			}
		}
	}

	rows, err := s.q.ListActiveListings(ctx, f)
	if err != nil {
		return connect.NewError(connect.CodeInternal, err)
	}

	for i := range rows {
		l := &Listing{
			Collection:         rows[i].Collection,
			TokenId:            rows[i].TokenID,
			Seller:             rows[i].Seller,
			PriceWei:           rows[i].PriceWei,
			Amount:             rows[i].Amount,
			Standard:           rows[i].Standard,
			TxHash:             rows[i].TxHash,
			Name:               rows[i].Name,
			ImageUri:           rows[i].ImageURI,
			CollectionVerified: rows[i].CollectionVerified,
		}
		if !rows[i].ExpiresAt.IsZero() {
			l.ExpiresAtMs = rows[i].ExpiresAt.UnixMilli()
		}
		if !rows[i].ListedAt.IsZero() {
			l.ListedAtMs = rows[i].ListedAt.UnixMilli()
		}
		if err := stream.Send(l); err != nil {
			return err
		}
	}
	return nil
}

